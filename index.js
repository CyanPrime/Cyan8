io.on('connection', function (socket) {

	socket.on('chat message', function(msg){
		console.log('chat message: ' + msg);
		socket.broadcast.emit('chat message', msg);
		socket.emit('chat message', msg);
	});
	
	socket.on('be_player', function(msg){
		console.log('be_player: ' + msg);

		if(playerOne == -1){
			playerOne = socket.id;
			socket.emit('be_player', 'success 1');
			socket.emit('conjson', controlJSON);
		}
		
		else if(playerTwo == -1){
			playerTwo = socket.id;
			socket.emit('be_player', 'success 2');
			socket.emit('conjson', controlJSON);
		}
		
		else socket.emit('be_player', 'fail: game full');
	});
	
	socket.on('game_bin', function(msg){
		console.log('game_bin: ' + msg.name);
		
		controlJSON = require('./json/' + msg.name + '_2P.json');
		
		socket.broadcast.emit('game_bin', msg);
		socket.emit('game_bin', msg);
		game = msg;
		
		chip8.reset(); //(?)
		chip8.loadGame(msg.payload);
		
		hasGame = true;
	});
  
	socket.on('disconnect', function(){
		if(socket.id == playerOne) playerOne = -1;
		if(socket.id == playerTwo) playerTwo = -1;
		console.log('user disconnected');
	});
	
	socket.on('keydown', function(msg){
		if(socket.id == playerOne || socket.id == playerTwo){
			chip8.key[chip8.keyMap[msg]] = true;
			chip8.currentKey = chip8.keyMap[msg];
		}
		//console.log('keydown');
	});
	
	socket.on('keyup', function(msg){
		if(socket.id == playerOne || socket.id == playerTwo){
			chip8.key[chip8.keyMap[msg]] = false;
			chip8.currentKey = false;
		}
		//console.log('keyup');
	});
});

